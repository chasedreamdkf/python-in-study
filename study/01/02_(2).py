a, b = input("请输入数字:\n").split()
a = eval(a)
b = eval(b)
print(f'{a}+{b}={a + b}')
print('{0}+{1}={2}'.format(a, b, a + b))
print(f'{a}-{b}={a - b}')
print('{0}-{1}={2}'.format(a, b, a - b))
print(f'{a}*{b}={a * b}')
print('{0}*{1}={2}'.format(a, b, a * b))
print(f'{a}/{b}={a / b}')
print('{0}/{1}={2}'.format(a, b, a / b))
